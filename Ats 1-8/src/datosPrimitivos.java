public class datosPrimitivos {
    //Datos primitivos    NO almacena null
    public static void main (String[] args){
        byte enteroByte =-128; //byte:8 -128 a 127
        System.out.println(enteroByte);
        short enteroShort =34; //-32768 a 32767
        System.out.println(enteroShort);
        int enteroInt=12;
        System.out.println(enteroInt);
        float flotante=4.94f;//INDISPENSABLE f
        System.out.println(flotante);
        double flotanteDoble=12.533134;
        System.out.println(flotanteDoble);
        char caracter='a';//Comillas simples
        System.out.println(caracter);
        boolean decision=true;
        System.out.println(decision);

    }
}
